CREATE DATABASE TRANSPORTE;
USE TRANSPORTE;
CREATE TABLE CLIENTE
(
		ID_CLIENTE NUMERIC(5,0) NOT NULL,
        CPF_CLI VARCHAR (11) NOT NULL PRIMARY KEY,
        NOME_CLI VARCHAR (50) NOT NULL,
        CIDADE_CLI VARCHAR (30) NOT NULL,
        UF_CLI CHAR (2) NOT NULL,
        DATA_C_M DATETIME(6) NOT NULL,
        OBSERVACAO VARCHAR(200)
);
CREATE TABLE MOTORISTA
(
		COD_MOTORISTA NUMERIC(5,0) NOT NULL PRIMARY KEY,
        NOME_M VARCHAR (50) NOT NULL,
        ENDEREÇO_M VARCHAR (30) NOT NULL,
        NUMERO_M NUMERIC(4) NOT NULL,
        BAIRRO_M VARCHAR(30) NOT NULL,
        CIDADE_M VARCHAR (30) NOT NULL,
        UF_M CHAR (2) NOT NULL,
        CEP_M CHAR (8) NOT NULL,
        FONE_M VARCHAR(20) NOT NULL,
        DATA_N_M DATETIME NOT NULL,
        CNH_M DATETIME NOT NULL,
        CATEGORIA_M VARCHAR(11) NOT NULL,
        EMAIL_M VARCHAR(80) NOT NULL
);
CREATE TABLE ONIBUS
(
		ID_PLACA VARCHAR(7) NOT NULL PRIMARY KEY,
        MARCA VARCHAR(20) NOT NULL,
        TIPO CHAR(1) NOT NULL,
        ANO NUMERIC(4) NOT NULL,
        OBSERVACAO VARCHAR(200),
        ASSENTO NUMERIC(3) NOT NULL
);
CREATE TABLE PERCURSO
(
		ID_PERCURSO VARCHAR(7)  NOT NULL,
		ROTA VARCHAR(60) NOT NULL PRIMARY KEY,
		TEMPO_P VARCHAR (10) NOT NULL, 
		TROCA_M CHAR (1) NOT NULL,
		NUM_MOT NUMERIC(2) NOT NULL,
		PERCURSO CHAR (1) NOT NULL,
		VALOR_B NUMERIC (12,2) NOT NULL,
		VALOR_SB NUMERIC (12,2) NOT NULL
		
);
CREATE TABLE USUARIO
(
		NOME_US VARCHAR(40) NOT NULL,
		TIPO_US CHAR (1) NOT NULL,
		USER_ID VARCHAR(15) NOT NULL,
		SENHA_ID VARCHAR(10)NOT NULL
);
CREATE TABLE LANCAMENTO_VIAGEM
(
		DATA_VIAGEM DATETIME PRIMARY KEY NOT NULL,
		ROTA VARCHAR (60) NOT NULL,
        ID_PLACA VARCHAR(7)NOT NULL,
		COD_MOTORISTA NUMERIC(5,0)  NOT NULL
);
CREATE TABLE LANCAMENTO_VENDAS
(
		DATA_VIAGEM DATETIME NOT NULL,
        ROTAS VARCHAR (60) NOT NULL,
		ASSENTO NUMERIC(3) NOT NULL,
		NUM_CPF_CLI VARCHAR (11) NOT NULL,
		BAGAGEM NUMERIC(3) NOT NULL
);

ALTER TABLE LANCAMENTO_VIAGEM
ADD	FOREIGN KEY ID_PLACA(ID_PLACA) REFERENCES  ONIBUS(ID_PLACA);

ALTER TABLE LANCAMENTO_VENDAS
ADD FOREIGN KEY NUM_CPF_CLI(NUM_CPF_CLI) REFERENCES CLIENTE(CPF_CLI); 

ALTER TABLE LANCAMENTO_VIAGEM
ADD FOREIGN KEY COD_MOTORISTA(COD_MOTORISTA) REFERENCES MOTORISTA(COD_MOTORISTA);

ALTER TABLE LANCAMENTO_VENDAS
ADD FOREIGN KEY DATA_VIAGEM(DATA_VIAGEM) REFERENCES LANCAMENTO_VIAGEM(DATA_VIAGEM);

ALTER TABLE LANCAMENTO_VIAGEM
ADD FOREIGN KEY ROTA(ROTA) REFERENCES PERCURSO(ROTA);

ALTER TABLE LANCAMENTO_VENDAS
ADD FOREIGN KEY ROTAS(ROTAS) REFERENCES PERCURSO(ROTA);





